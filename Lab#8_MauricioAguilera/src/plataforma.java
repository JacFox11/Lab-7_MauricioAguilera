    private void jButton10MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton10MouseClicked
        // TODO add your handling code here:
        try {
            String nombre;
            int edad;
            String sexo="";
            Platillo entrada;
            Platillo sopa;
            Platillo fuerte;
            Platillo postre;

            nombre = tf_nombrecliente.getText();
            edad = (Integer) sp_edadcliente.getValue();
            if (rb_mcliente.isSelected()){
                sexo="M";
            }
            else{
                sexo ="F";
            }
            entrada = (Platillo) cb_cliente_entrada.getSelectedItem();
            sopa = (Platillo) cb_cliente_sopa.getSelectedItem();
            fuerte = (Platillo) cb_cliente_fuerte.getSelectedItem();
            postre = (Platillo) cb_cliente_postre.getSelectedItem();
            
            clientes.add(new Cliente(nombre, edad, sexo, new Orden (entrada, sopa, fuerte, postre)));
            
            DefaultTableModel temp = (DefaultTableModel) jt_cliente.getModel();
            Object[] temp2 = new Object[4];
            temp2[0] = nombre;
            temp2[1] = entrada.getNombre();
            temp2[2] = sopa.getNombre();
            temp2[3] = fuerte.getNombre();
            temp2[4] = postre.getNombre();
            temp.addRow(temp2);
            jt_cliente.setModel(temp);
        } catch (Exception e) {
        }
        
    }//GEN-LAST:event_jButton10MouseClicked

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        DefaultComboBoxModel cb1 = (DefaultComboBoxModel) cb_cliente_entrada.getModel();
        DefaultComboBoxModel cb2 = (DefaultComboBoxModel) cb_cliente_sopa.getModel();
        DefaultComboBoxModel cb3 = (DefaultComboBoxModel) cb_cliente_fuerte.getModel();
        DefaultComboBoxModel cb4 = (DefaultComboBoxModel) cb_cliente_postre.getModel();
        ap.cargarArchivo();
        for (Platillo t : platillos) {
            if (t.getTipo().equals("entrada")){
                cb1.addElement(t);
            }
            if (t.getTipo().equals("sopa")){
                cb2.addElement(t);
            }
            if (t.getTipo().equals("fuerte")){
                cb3.addElement(t);
            }
            if (t.getTipo().equals("postre")){
                cb4.addElement(t);
            }
            cb_cliente_entrada.setModel(cb1);
            cb_cliente_sopa.setModel(cb2);
            cb_cliente_fuerte.setModel(cb3);
            cb_cliente_postre.setModel(cb4);
        }
        jd_nuevocliente.setModal(true);
        jd_nuevocliente.pack();
        jd_nuevocliente.setLocationRelativeTo(this);
        jd_nuevocliente.setVisible(true);
    }//GEN-LAST:event_jButton2MouseClicked

